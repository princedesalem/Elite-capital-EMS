ALTER TABLE EVALUATION              MODIFY COLUMN reviewee_id VARCHAR(32) NOT NULL;
ALTER TABLE FICHE_POSTE             MODIFY COLUMN manager_id VARCHAR(32) NULL;
ALTER TABLE FICHE_POSTE             MODIFY COLUMN employee_id VARCHAR(32) NULL;
