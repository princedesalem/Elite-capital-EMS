ALTER TABLE USER_SETTINGS           MODIFY COLUMN user_id VARCHAR(32) NOT NULL;
ALTER TABLE MODULE_STORE_PURCHASE   MODIFY COLUMN created_by VARCHAR(32) NULL;
ALTER TABLE PERSISTENCE_MODULE      MODIFY COLUMN created_by VARCHAR(32) NULL;

